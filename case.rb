weather = 'sunny'
 
case weather
when 'sunny'
  puts "Slap on sunscreen and enjoy that solar radiation!"
when 'rainy'
    puts "Rain equals wet and messy, so wear raingear and bring your allergy stuff"
when 'foggy'
  puts "Make sure your headlights work so you can see in this fog!"
when 'snowy'
    puts "Make sure you dress warmly, have snowboots and get hot cocoa!"
else
  puts "Can't tell, so better wear all-weather gear!"
end